class Wildnest_Font_Icons
{
	static $_instance;
	static $_icons;
	static $enqueued;
	private $picked_types = array();

	static function get_instance()
	{
		if (is_null(self::$_instance)) {
			self::$_instance = new self();
		}

		return self::$_instance;
	}

	/**
	 * Get support icons
	 *
	 * @return array
	 */
	function get_icons()
	{
		if (is_null(self::$_icons)) {
			$ver = get_option('wildnest_fa_ver', 'v4');

			$icons = [];

			$icons['font-awesome-v456'] = array(
				'name'         => esc_html__('Font Awesome v6', 'wildnest'),
				'icons'        => $this->get_font_awesome_icons6(),
				'url'          => array(
					'font-awesome' => esc_url(get_template_directory_uri()) . '/assets/vendor/font-awesome/all.min.css',
				),
				'class_config' => '__icon_name__',
			);
		
			$icons['linear-icons-v100'] = array(
				'name'         => esc_html__('Linear Icons (Free)', 'wildnest'),
				'icons'        => $this->get_linear_icons_icons(),
				'url'          => array(
					'linear-icons' => esc_url(get_template_directory_uri()) . '/assets/vendor/linear-icons/linear-icons.css',
				),
				'class_config' => '__icon_name__',
			);

			self::$_icons = apply_filters('wildnest/customizer/font_icons', $icons);
		}

		return self::$_icons;
	}

	function is_used()
	{
		if (is_customize_preview()) {
			return true;
		}
		$check = apply_filters('wildnest/load-icons', true);
		if (is_null($check)) {
			$builders = array('header_builder_panel', 'footer_builder_panel');
			$list     = apply_filters('wildnest/icon_used', array());

			foreach ($builders as $setting_key) {
				$data = Wildnest()->get_setting($setting_key);
				if (is_array($data)) {
					foreach ($data as $devices => $rows) {
						foreach ((array) $rows as $row_id => $items) {
							foreach ((array) $items as $item) {
								if (is_array($item)) {
									if (isset($list[$item['id']]) && $list[$item['id']]) {
										return true;
									}
								}
							}
						}
					}
				}
			}
		}

		return $check;
	}

	function enqueue()
	{
		if ($this->is_used()) {
			if (is_null(self::$enqueued)) {
				/**
				 * @since 0.3.9
				 */
				wp_dequeue_style('awf-fontawesome');
				foreach ($this->get_icons() as $icon_id => $icon) {
					if (!is_array($icon['url'])) {
						wp_dequeue_style($icon_id);
						wp_enqueue_style($icon_id, $icon['url'], array(), '5.0.0');
					} else {
						foreach ($icon['url'] as $id => $url) {
							wp_dequeue_style($id);
							wp_enqueue_style($id, $url, array(), '5.0.0');
						}
					}
				}
				self::$enqueued = true;
			}
		}
	}


